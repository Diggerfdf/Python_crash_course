# 4.2 Animals: Think of at least three different animals
# that have a common characteristic. Store the names of these animals in a list, 
# and use a for loop to print out the name of each animal. 

animals = ['cat', 'dog', 'sheep']

for animal in animals:
	print(animal)
	print('A {} would make a great pet!\n'.format(animal))
	
print('\nAny of these animals would make a great pet!')
